#Checking data types

# Int
print(type(10))
# Float                  
print(type(10.25))
# Complex               
print(type(3j))
# String             
print(type('Neeraj'))
# List          
print(type([1, 2, 3]))
# Dictionary           
print(type({'name':'Neeraj', 'Age':'22'}))
# Set
print(type({9.8, 3.14, 2.7}))  
# Tuple  
print(type((9.8, 3.14, 2.7)))    